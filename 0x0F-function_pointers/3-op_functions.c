#include<stdio.h>


/**
 *op_add - sum of a and b
 *Description:sum of two intigers and return the sum
 *@a:first number
 *@b:second number
 *Return:sum
 */

int op_add(int a, int b)
{
	return (a + b);
}



/**
 *op_sub - difference of a and b
 *Description:differane of two intigers and return the sum
 *@a:first number
 *@b:second number
 *Return:sub
 */


int op_sub(int a, int b)
{
	return (a - b);
}


/**
 *op_mul - multply of a and b
 *Description:multply of two intigers and return the sum
 *@a:first number
 *@b:second number
 *Return:multiplication result
 */
int op_mul(int a, int b)
{
	return (a * b);
}



/**
 *op_div - division of a and b
 *Description:division of two intigers and return the sum
 *@a:first number
 *@b:second number
 *Return:division
 */

int op_div(int a, int b)
{
	return (a / b);
}






/**
 *op_mod - mod of a and b
 *Description:mod of two intigers and return the sum
 *@a:first number
 *@b:second number
 *Return:mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
